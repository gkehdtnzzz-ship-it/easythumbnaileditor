function bindDropTarget(target, slot) {
  ["dragenter", "dragover"].forEach((name) => {
    target.addEventListener(name, (event) => {
      event.preventDefault();
      event.stopPropagation();
      if (event.dataTransfer) event.dataTransfer.dropEffect = "copy";
      target.classList.add("dragging");
      if (target !== els.dropZone) els.dropZone.classList.add("slot-dragging");
    });
  });

  target.addEventListener("dragleave", (event) => {
    event.preventDefault();
    event.stopPropagation();
    target.classList.remove("dragging");
    if (target !== els.dropZone) els.dropZone.classList.remove("slot-dragging");
  });

  target.addEventListener("drop", (event) => {
    event.preventDefault();
    event.stopPropagation();
    clearDropHighlights();
    const file = getDroppedFile(event);
    if (file) loadFile(file, slot);
  });
}

function handleGlobalDragOver(event) {
  if (!hasDraggedFile(event)) return;
  event.preventDefault();
  if (event.dataTransfer) event.dataTransfer.dropEffect = "copy";
  const target = getDropTargetFromPoint(event);
  clearDropHighlights();
  target?.classList.add("dragging");
  if (target && target !== els.dropZone) els.dropZone.classList.add("slot-dragging");
}

function handleGlobalDrop(event) {
  if (!hasDraggedFile(event)) return;
  event.preventDefault();
  event.stopPropagation();
  const file = getDroppedFile(event);
  const target = getDropTargetFromPoint(event);
  const slot = target?.dataset.uploadSlot || "main";
  clearDropHighlights();
  if (file) loadFile(file, slot);
}

function hasDraggedFile(event) {
  const types = Array.from(event.dataTransfer?.types || []);
  return types.includes("Files");
}

function getDropTargetFromPoint(event) {
  const element = document.elementFromPoint(event.clientX, event.clientY);
  return element?.closest?.("[data-upload-slot]") || els.canvasDropZone || els.dropZone;
}

function clearDropHighlights() {
  els.dropZone.classList.remove("dragging", "slot-dragging");
  els.canvasDropZone.classList.remove("dragging");
  document.querySelectorAll("[data-upload-slot].dragging").forEach((node) => node.classList.remove("dragging"));
}

