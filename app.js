function estimateSize(dataUrl) {
  const bytes = estimateBytes(dataUrl);
  els.fileEstimate.textContent = bytes > 1024 * 1024
    ? `${(bytes / 1024 / 1024).toFixed(2)} MB ${t("estimated")}`
    : `${Math.max(1, Math.round(bytes / 1024))} KB ${t("estimated")}`;
}

function estimateBytes(dataUrl) {
  return Math.round((dataUrl.length - dataUrl.indexOf(",") - 1) * 0.75);
}

function downloadImage() {
  if (getExportMode() === "split3x3") {
    downloadSplitImages();
    return;
  }
  const format = state.preset.kind === "idPhoto" ? "image/jpeg" : getFormat();
  const ext = format.includes("png") ? "png" : format.includes("webp") ? "webp" : "jpg";
  const link = document.createElement("a");
  link.href = state.preset.kind === "idPhoto" ? getIdPhotoDownloadUrl() : getCleanCanvasDataUrl(format, getQuality());
  link.download = `${state.imageName || "easy-thumbnail"}-${state.preset.id}-${state.orientation}.${ext}`;
  link.click();
}

function getExportMode() {
  if (!hasSplitConverter()) return "single";
  return document.querySelector("input[name='exportMode']:checked")?.value || "single";
}

function hasSplitConverter() {
  return ["instagram-profile", "x-profile", "tiktok-profile"].includes(state.preset.id);
}

function downloadSplitImages() {
  const format = state.preset.kind === "idPhoto" ? "image/jpeg" : getFormat();
  const ext = format.includes("png") ? "png" : format.includes("webp") ? "webp" : "jpg";
  const tileSize = getSplitTileSize();
  const source = getCleanCanvasElement(tileSize * 3, tileSize * 3);

  for (let row = 0; row < 3; row += 1) {
    for (let col = 0; col < 3; col += 1) {
      const tile = document.createElement("canvas");
      const context = tile.getContext("2d");
      tile.width = tileSize;
      tile.height = tileSize;
      context.drawImage(
        source,
        col * tileSize,
        row * tileSize,
        tileSize,
        tileSize,
        0,
        0,
        tileSize,
        tileSize
      );
      const link = document.createElement("a");
      link.href = tile.toDataURL(format, getQuality());
      link.download = `${state.imageName || "easy-thumbnail"}-3x3-r${row + 1}-c${col + 1}.${ext}`;
      setTimeout(() => link.click(), (row * 3 + col) * 90);
    }
  }
}

function getSplitTileSize() {
  return 1080;
}

function getIdPhotoDownloadUrl() {
  const maxSize = state.idPhoto.maxSize;
  let quality = getQuality();
  let url = getCleanCanvasDataUrl("image/jpeg", quality);
  if (!maxSize) return url;
  while (estimateBytes(url) > maxSize && quality > 0.42) {
    quality -= 0.06;
    url = getCleanCanvasDataUrl("image/jpeg", quality);
  }
  return url;
}

function getCleanCanvasDataUrl(format, quality) {
  return getCleanCanvasElement().toDataURL(format, quality);
}

function getCleanCanvasElement(outputWidth = state.preset.width, outputHeight = state.preset.height) {
  const canvas = document.createElement("canvas");
  const context = canvas.getContext("2d");
  const { width, height } = state.preset;
  const scaleX = outputWidth / width;
  const scaleY = outputHeight / height;
  canvas.width = outputWidth;
  canvas.height = outputHeight;
  context.fillStyle = state.theme === "dark" ? "#151d26" : "#ffffff";
  context.fillRect(0, 0, outputWidth, outputHeight);
  if (state.image) {
    if (state.autoFit) drawCoverBackgroundToContext(context, outputWidth, outputHeight);
    context.drawImage(
      state.image,
      state.offsetX * scaleX,
      state.offsetY * scaleY,
      state.imageWidth * scaleX,
      state.imageHeight * scaleY
    );
  } else {
    context.fillStyle = state.theme === "dark" ? "#151d26" : "#edf3f8";
    context.fillRect(0, 0, outputWidth, outputHeight);
  }
  return canvas;
}

function startDrag(event) {
  if (!state.image) return;
  const point = getCanvasPoint(event);
  const handle = getHandleAtPoint(point);
  const inside = isPointInsideImage(point);
  if (!handle && !inside) {
    state.imageSelected = false;
    render();
    return;
  }
  state.imageSelected = true;
  state.interactionMode = handle ? "resize" : "move";
  state.activeHandle = handle?.id || null;
  if (handle) setAutoFit(false);
  state.dragging = true;
  els.canvas.setPointerCapture(event.pointerId);
  state.dragStart = point;
  state.dragStartImage = {
    x: state.offsetX,
    y: state.offsetY,
    width: state.imageWidth,
    height: state.imageHeight
  };
  event.preventDefault();
  render();
}

function moveDrag(event) {
  const point = getCanvasPoint(event);
  if (!state.dragging) {
    updateCanvasCursor(point);
    return;
  }
  if (state.autoFit) setAutoFit(false);
  if (state.interactionMode === "resize") {
    resizeImageFromPoint(point);
  } else {
    state.offsetX = Math.round(state.dragStartImage.x + point.x - state.dragStart.x);
    state.offsetY = Math.round(state.dragStartImage.y + point.y - state.dragStart.y);
  }
  syncInputs();
  syncScaleFromSize();
  render();
}

function endDrag(event) {
  state.dragging = false;
  state.interactionMode = null;
  state.activeHandle = null;
  if (event?.clientX !== undefined) updateCanvasCursor(getCanvasPoint(event));
}

function resizeImageFromPoint(point) {
  const handle = state.activeHandle || "";
  const start = state.dragStartImage;
  const dx = point.x - state.dragStart.x;
  const dy = point.y - state.dragStart.y;
  let x = start.x;
  let y = start.y;
  let width = start.width;
  let height = start.height;

  if (handle.includes("e")) width = start.width + dx;
  if (handle.includes("s")) height = start.height + dy;
  if (handle.includes("w")) {
    width = start.width - dx;
    x = start.x + dx;
  }
  if (handle.includes("n")) {
    height = start.height - dy;
    y = start.y + dy;
  }

  const keepRatio = handle.length === 2;
  if (keepRatio) {
    const ratio = start.width / start.height || 1;
    const widthFromHeight = height * ratio;
    const heightFromWidth = width / ratio;
    if (Math.abs(dx) > Math.abs(dy)) {
      height = heightFromWidth;
    } else {
      width = widthFromHeight;
    }
    if (handle.includes("w")) x = start.x + start.width - width;
    if (handle.includes("n")) y = start.y + start.height - height;
  }

  const minSize = 24;
  if (width < minSize) {
    if (handle.includes("w")) x = start.x + start.width - minSize;
    width = minSize;
  }
  if (height < minSize) {
    if (handle.includes("n")) y = start.y + start.height - minSize;
    height = minSize;
  }

  state.offsetX = Math.round(x);
  state.offsetY = Math.round(y);
  state.imageWidth = Math.round(width);
  state.imageHeight = Math.round(height);
}

function getHandleAtPoint(point) {
  if (!state.imageSelected) return null;
  return getResizeHandles().find((handle) => {
    const half = handle.size * 0.75;
    return Math.abs(point.x - handle.x) <= half && Math.abs(point.y - handle.y) <= half;
  }) || null;
}

function isPointInsideImage(point) {
  return point.x >= state.offsetX
    && point.x <= state.offsetX + state.imageWidth
    && point.y >= state.offsetY
    && point.y <= state.offsetY + state.imageHeight;
}

function updateCanvasCursor(point) {
  const handle = getHandleAtPoint(point);
  if (handle) {
    els.canvas.style.cursor = getResizeCursor(handle.id);
  } else if (isPointInsideImage(point)) {
    els.canvas.style.cursor = "move";
  } else {
    els.canvas.style.cursor = "default";
  }
}

function getResizeCursor(handleId) {
  const cursors = {
    n: "ns-resize",
    s: "ns-resize",
    e: "ew-resize",
    w: "ew-resize",
    ne: "nesw-resize",
    sw: "nesw-resize",
    nw: "nwse-resize",
    se: "nwse-resize"
  };
  return cursors[handleId] || "default";
}

function getCanvasPoint(event) {
  const rect = els.canvas.getBoundingClientRect();
  return {
    x: ((event.clientX - rect.left) / rect.width) * els.canvas.width,
    y: ((event.clientY - rect.top) / rect.height) * els.canvas.height
  };
}

