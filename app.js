function downloadSplitImages() {
  const format = state.preset.kind === "idPhoto" ? "image/jpeg" : getFormat();
  const ext = format.includes("png") ? "png" : format.includes("webp") ? "webp" : "jpg";
  const tileSize = getSplitTileSize();
  const source = getCleanCanvasElement(tileSize * 3, tileSize * 3);

  for (let row = 0; row < 3; row += 1) {
    for (let col = 0; col < 3; col += 1) {
      const tile = document.createElement("canvas");
      const context = tile.getContext("2d");
      tile.width = tileSize;
      tile.height = tileSize;
      context.drawImage(
        source,
        col * tileSize,
        row * tileSize,
        tileSize,
        tileSize,
        0,
        0,
        tileSize,
        tileSize
      );
      const link = document.createElement("a");
      link.href = tile.toDataURL(format, getQuality());
      link.download = `${state.imageName || "easy-thumbnail"}-3x3-r${row + 1}-c${col + 1}.${ext}`;
      setTimeout(() => link.click(), (row * 3 + col) * 90);
    }
  }
}

