function syncTargetInputs() {
  els.targetWidthInput.value = String(state.preset.width);
  els.targetHeightInput.value = String(state.preset.height);
  const isCustom = state.preset.id === "custom";
  els.targetWidthInput.disabled = !isCustom;
  els.targetHeightInput.disabled = !isCustom;
}

function syncScaleFromSize() {
  const fit = getFitSize();
  const scale = fit.width ? state.imageWidth / fit.width : 1;
  els.scaleRange.value = String(Math.max(10, Math.min(300, Math.round(scale * 100))));
}

function render() {
  const { width, height } = state.preset;
  els.canvas.width = width;
  els.canvas.height = height;
  ctx.clearRect(0, 0, width, height);
  ctx.fillStyle = state.theme === "dark" ? "#151d26" : "#ffffff";
  ctx.fillRect(0, 0, width, height);

  if (state.image) {
    if (state.autoFit) drawCoverBackground(width, height);
    ctx.drawImage(state.image, state.offsetX, state.offsetY, state.imageWidth, state.imageHeight);
    if (state.imageSelected) drawSelectionBox();
  } else {
    drawPlaceholder(width, height);
  }

  updateMeta();
  updatePreview();
}

function drawSelectionBox() {
  const handles = getResizeHandles();
  ctx.save();
  ctx.strokeStyle = "#00a878";
  ctx.lineWidth = Math.max(2, Math.round(Math.min(state.preset.width, state.preset.height) / 260));
  ctx.setLineDash([10, 6]);
  ctx.strokeRect(state.offsetX, state.offsetY, state.imageWidth, state.imageHeight);
  ctx.setLineDash([]);
  handles.forEach((handle) => {
    ctx.fillStyle = "#ffffff";
    ctx.strokeStyle = "#00a878";
    ctx.lineWidth = 2;
    ctx.fillRect(handle.x - handle.size / 2, handle.y - handle.size / 2, handle.size, handle.size);
    ctx.strokeRect(handle.x - handle.size / 2, handle.y - handle.size / 2, handle.size, handle.size);
  });
  ctx.restore();
}

function getResizeHandles() {
  const x = state.offsetX;
  const y = state.offsetY;
  const w = state.imageWidth;
  const h = state.imageHeight;
  const size = Math.max(12, Math.min(22, Math.round(Math.min(state.preset.width, state.preset.height) / 30)));
  return [
    { id: "nw", x, y, size },
    { id: "n", x: x + w / 2, y, size },
    { id: "ne", x: x + w, y, size },
    { id: "e", x: x + w, y: y + h / 2, size },
    { id: "se", x: x + w, y: y + h, size },
    { id: "s", x: x + w / 2, y: y + h, size },
    { id: "sw", x, y: y + h, size },
    { id: "w", x, y: y + h / 2, size }
  ];
}

function drawCoverBackground(width, height) {
  drawCoverBackgroundToContext(ctx, width, height);
}

function drawCoverBackgroundToContext(context, width, height) {
  const srcWidth = state.sourceWidth || state.image.naturalWidth;
  const srcHeight = state.sourceHeight || state.image.naturalHeight;
  const scale = Math.max(width / srcWidth, height / srcHeight);
  const drawWidth = srcWidth * scale;
  const drawHeight = srcHeight * scale;
  context.save();
  context.globalAlpha = 0.34;
  context.filter = "blur(18px)";
  context.drawImage(state.image, (width - drawWidth) / 2, (height - drawHeight) / 2, drawWidth, drawHeight);
  context.filter = "none";
  context.globalAlpha = 1;
  context.fillStyle = state.theme === "dark" ? "rgba(10, 16, 22, 0.28)" : "rgba(255, 255, 255, 0.18)";
  context.fillRect(0, 0, width, height);
  context.restore();
}

