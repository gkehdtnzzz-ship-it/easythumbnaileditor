function getOriginalPreviewUrl() {
  if (!state.image) return "";
  const canvas = document.createElement("canvas");
  const context = canvas.getContext("2d");
  canvas.width = state.sourceWidth;
  canvas.height = state.sourceHeight;
  context.drawImage(state.image, 0, 0);
  return canvas.toDataURL("image/jpeg", 0.88);
}

function getSlotPreviewUrl(slot, fallbackUrl, width = state.preset.width, height = state.preset.height) {
  const source = slot === "profile" ? state.profileImage : state.image;
  if (!source) return fallbackUrl;
  const canvas = document.createElement("canvas");
  const context = canvas.getContext("2d");
  canvas.width = width;
  canvas.height = height;
  context.fillStyle = state.theme === "dark" ? "#151d26" : "#ffffff";
  context.fillRect(0, 0, width, height);
  if (state.autoFit) {
    const coverScale = Math.max(width / source.naturalWidth, height / source.naturalHeight);
    const coverWidth = source.naturalWidth * coverScale;
    const coverHeight = source.naturalHeight * coverScale;
    context.save();
    context.globalAlpha = 0.34;
    context.filter = "blur(18px)";
    context.drawImage(source, (width - coverWidth) / 2, (height - coverHeight) / 2, coverWidth, coverHeight);
    context.filter = "none";
    context.globalAlpha = 1;
    context.fillStyle = state.theme === "dark" ? "rgba(10, 16, 22, 0.28)" : "rgba(255, 255, 255, 0.18)";
    context.fillRect(0, 0, width, height);
    context.restore();
  }
  const scale = state.autoFit
    ? Math.min(width / source.naturalWidth, height / source.naturalHeight)
    : Math.max(width / source.naturalWidth, height / source.naturalHeight);
  const drawWidth = source.naturalWidth * scale;
  const drawHeight = source.naturalHeight * scale;
  context.drawImage(source, (width - drawWidth) / 2, (height - drawHeight) / 2, drawWidth, drawHeight);
  return canvas.toDataURL(getFormat(), getQuality());
}

