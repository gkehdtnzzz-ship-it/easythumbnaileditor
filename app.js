function rebuildPresetButtons() {
  els.presetButtons.innerHTML = "";
  const groups = presets.reduce((map, preset) => {
    const sizeKey = preset.id === "steam-profile" || preset.id === "steam-store"
      ? "Steam"
      : `${preset.width} x ${preset.height}`;
    if (!map.has(sizeKey)) map.set(sizeKey, []);
    map.get(sizeKey).push(preset);
    return map;
  }, new Map());

  groups.forEach((groupPresets, sizeKey) => {
    const group = div("preset-group");
    const title = document.createElement("p");
    title.className = "preset-size";
    title.textContent = sizeKey === "Steam" ? "Steam" : sizeKey;
    const buttons = div("preset-button-row");
    groupPresets.forEach((preset) => {
      const button = document.createElement("button");
      button.type = "button";
      button.className = "preset-button";
      button.dataset.presetId = preset.id;
      button.textContent = t(`preset.${preset.key}`);
      button.classList.toggle("is-active", preset.id === state.preset.id);
      buttons.append(button);
    });
    group.append(title, buttons);
    els.presetButtons.append(group);
  });
}

