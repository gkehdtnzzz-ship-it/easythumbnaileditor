function applyPresetDimensions() {
  if (state.preset.id === "play-listing") {
    const size = playSizes[state.orientation];
    state.preset.width = size.width;
    state.preset.height = size.height;
  } else if (state.preset.id === "id-photo") {
    const size = getIdPhotoPixelSize();
    state.preset.width = size.width;
    state.preset.height = size.height;
  } else if (state.preset.id === "steam-store") {
    const asset = getCurrentSteamAsset();
    state.preset.width = asset.width;
    state.preset.height = asset.height;
  }
}

function getCurrentSteamAsset() {
  return steamStoreAssets.find((asset) => asset.id === state.steamAsset) || steamStoreAssets[0];
}

function syncIdPhotoSettings() {
  state.idPhoto.width = Math.max(1, Number(els.idPhotoWidth.value) || 1);
  state.idPhoto.height = Math.max(1, Number(els.idPhotoHeight.value) || 1);
  state.idPhoto.unit = els.idPhotoUnit.value;
  state.idPhoto.dpi = Math.max(72, Number(els.idPhotoDpi.value) || 300);
  state.idPhoto.maxSize = Number(els.idPhotoMaxSize.value) || 0;
  state.idPhoto.quality = Number(els.idPhotoQuality.value) / 100;
}

function getIdPhotoPixelSize() {
  if (state.idPhoto.unit === "cm") {
    return {
      width: Math.max(1, Math.round((state.idPhoto.width / 2.54) * state.idPhoto.dpi)),
      height: Math.max(1, Math.round((state.idPhoto.height / 2.54) * state.idPhoto.dpi))
    };
  }
  return {
    width: Math.max(1, Math.round(state.idPhoto.width)),
    height: Math.max(1, Math.round(state.idPhoto.height))
  };
}

function loadFile(file, slot = "main") {
  if (!isImageFile(file)) return;
  const reader = new FileReader();
  reader.onload = () => {
    const image = new Image();
    image.onload = () => {
      if (slot === "profile") {
        state.profileImage = image;
      } else {
        state.image = image;
        state.imageName = file.name.replace(/\.[^.]+$/, "");
        state.sourceWidth = image.naturalWidth;
        state.sourceHeight = image.naturalHeight;
        state.imageSelected = true;
        applyAutoSizing();
      }
      render();
    };
    image.src = reader.result;
  };
  reader.readAsDataURL(file);
}

function isImageFile(file) {
  if (file.type?.startsWith("image/")) return true;
  return /\.(png|jpe?g|webp|gif|bmp|avif)$/i.test(file.name || "");
}

function fitImageToPreset() {
  applyPresetDimensions();
  const fit = getFitSize();
  state.imageWidth = Math.round(fit.width);
  state.imageHeight = Math.round(fit.height);
  state.offsetX = Math.round((state.preset.width - state.imageWidth) / 2);
  state.offsetY = Math.round((state.preset.height - state.imageHeight) / 2);
  els.scaleRange.value = "100";
  syncInputs();
  syncTargetInputs();
}

function fitImageInsidePreset() {
  applyPresetDimensions();
  const fit = getContainSize();
  state.imageWidth = Math.round(fit.width);
  state.imageHeight = Math.round(fit.height);
  state.offsetX = Math.round((state.preset.width - state.imageWidth) / 2);
  state.offsetY = Math.round((state.preset.height - state.imageHeight) / 2);
  els.scaleRange.value = "100";
  syncInputs();
  syncTargetInputs();
}

function applyAutoSizing() {
  if (state.autoFit) {
    fitImageInsidePreset();
    return;
  }
  fitImageToPreset();
}

function getFitSize() {
  const srcWidth = state.sourceWidth || state.preset.width;
  const srcHeight = state.sourceHeight || state.preset.height;
  const scale = Math.max(state.preset.width / srcWidth, state.preset.height / srcHeight);
  return { width: srcWidth * scale, height: srcHeight * scale };
}

function getContainSize() {
  const srcWidth = state.sourceWidth || state.preset.width;
  const srcHeight = state.sourceHeight || state.preset.height;
  const scale = Math.min(state.preset.width / srcWidth, state.preset.height / srcHeight);
  return { width: srcWidth * scale, height: srcHeight * scale };
}

